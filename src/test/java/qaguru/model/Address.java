package qaguru.model;

public class Address {
    private String city;
    private String street;
    private String house;
    private String apartment;

    public Address() {
    }

    public String getCity() {
        return city;
    }

    public String getStreet() {
        return street;
    }

    public String getHouse() {
        return house;
    }

    public String getApartment() {
        return apartment;
    }
}